    private void fetchAndDisplayPassengerData() {
    try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/studentdb", "root", "");
        String sql = "SELECT * FROM managepassenger";
        PreparedStatement ptst = (PreparedStatement) con.prepareStatement(sql);
        ResultSet rs = ptst.executeQuery();
        DefaultTableModel dt = (DefaultTableModel) passengertable.getModel();
        dt.setRowCount(0); // Clear the table
        while (rs.next()) {
            Object[] row = {rs.getString("passengername"), rs.getString("gender"), rs.getString("nationality"), rs.getString("passportnumber"), rs.getString("phone")};
            dt.addRow(row);
        }
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }
}
