    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
        // Get the input values from the text fields
        String passengername = pn.getText();
        String gender = g.getText();
        String nationality = n.getText();
        String passportnumber = pass.getText();
        String phone = ph.getText();
        
        // Load the MySQL driver and establish a connection
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/studentdb", "root", "");
        
        // Prepare the SQL update statement
        String sql = "UPDATE managepassenger SET gender=?, nationality=?, passportnumber=?, phone=? WHERE passengername=?";
        PreparedStatement ptst = (PreparedStatement) con.prepareStatement(sql);
        
        // Set the parameters for the update statement
        ptst.setString(1, gender);
        ptst.setString(2, nationality);
        ptst.setString(3, passportnumber);
        ptst.setString(4, phone);
        ptst.setString(5, passengername);
        
        // Execute the update statement
        ptst.executeUpdate();
        
        // Show a success message
        JOptionPane.showMessageDialog(this, "Record Updated!");
        
        // Close the PreparedStatement and the connection
        ptst.close();
        con.close();
        
        // Refresh the JTable to reflect the changes
        fetchAndDisplayPassengerData();
    } catch (Exception e) {
        // Show an error message in case of any issues
        JOptionPane.showMessageDialog(this, e);
        e.printStackTrace();
    }
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/studentdb", "root", "");

        // Get the selected row index
        int selectedRowIndex = passengertable.getSelectedRow();
        if (selectedRowIndex != -1) {
            // Get the passenger name from the selected row
            String passengerName = passengertable.getValueAt(selectedRowIndex, 0).toString();
            
            // Prepare the DELETE SQL statement
            String sql = "DELETE FROM managepassenger WHERE passengername = ?";
            PreparedStatement ptst = (PreparedStatement) con.prepareStatement(sql);
            ptst.setString(1, passengerName);
            ptst.executeUpdate();
            
            // Remove the selected row from the JTable model
            DefaultTableModel model = (DefaultTableModel) passengertable.getModel();
            model.removeRow(selectedRowIndex);
            
            JOptionPane.showMessageDialog(this, "Data deleted successfully");
        } else {
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
        }

        con.close();
        
        // Refresh the JTable
        fetchAndDisplayPassengerData();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_deleteActionPerformed

