    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
         try {
        int selectedRowIndex = flighttable.getSelectedRow();
        if (selectedRowIndex != -1) {
            // Get the modified data from the text fields
            String flightCode = fc.getText();
            String source = s.getText();
            String destination = d.getText();
            String takeoff = tof.getText();
            String noofseat = nos.getText();

            // Load the MySQL driver and establish a connection
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/studentdb", "root", "");

            // Prepare the SQL update statement
            String sql = "UPDATE manageflight SET source=?, destination=?, takeoff=?, noofseat=? WHERE flightcode=?";
            PreparedStatement ptst = (PreparedStatement) con.prepareStatement(sql);
            ptst.setString(1, source);
            ptst.setString(2, destination);
            ptst.setString(3, takeoff);
            ptst.setString(4, noofseat);
            ptst.setString(5, flightCode);

            // Execute the update statement
            int rowsUpdated = ptst.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Record Updated!");
                
                // Update the flighttable with the modified data
                DefaultTableModel model = (DefaultTableModel) flighttable.getModel();
                model.setValueAt(source, selectedRowIndex, 1);
                model.setValueAt(destination, selectedRowIndex, 2);
                model.setValueAt(takeoff, selectedRowIndex, 3);
                model.setValueAt(noofseat, selectedRowIndex, 4);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update record!");
            }

            ptst.close();
            con.close();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a row to update");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }                                        
                                       
    
    }//GEN-LAST:event_updateActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/studentdb", "root", "");

        int selectedRowIndex = flighttable.getSelectedRow();
        if (selectedRowIndex != -1) {
            String flightCode = flighttable.getValueAt(selectedRowIndex, 0).toString();
            
            String sql = "DELETE FROM manageflight WHERE flightcode = ?";
            PreparedStatement ptst = (PreparedStatement) con.prepareStatement(sql);
            ptst.setString(1, flightCode);
            ptst.executeUpdate();
            
            DefaultTableModel model = (DefaultTableModel) flighttable.getModel();
            model.removeRow(selectedRowIndex);
            
            JOptionPane.showMessageDialog(this, "Data deleted successfully");
        } else {
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
        }

        con.close();
        
        // Refresh the JTable
        fetchAndDisplayData();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_deleteBtnActionPerformed

