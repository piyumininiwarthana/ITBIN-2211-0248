    private void fetchAndDisplayData() {
    try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/studentdb", "root", "");
        String sql = "SELECT * FROM manageflight";
        PreparedStatement ptst = (PreparedStatement) con.prepareStatement(sql);
        ResultSet rs = ptst.executeQuery();
        DefaultTableModel dt = (DefaultTableModel) flighttable.getModel();
        dt.setRowCount(0); // Clear the table
        while (rs.next()) {
            Object[] row = {rs.getString("flightcode"), rs.getString("source"), rs.getString("destination"), rs.getString("takeoff"), rs.getString("noofseat")};
            dt.addRow(row);
        }
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }
}

